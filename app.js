// celcius to fahrenhight 

let celci = parseInt(45);
let fahren = (celci * 9/5) + 32 ;

console.log( celci + ` Celcius temperature = `+ fahren + ` Fahrenhight. `);

// Fahrenhight to celsius 

let Fahrenhight =parseInt( 113);
let celsius = ((Fahrenhight - 32)* 5 )/9 ;
console.log( Fahrenhight + ` Fahrenhight temperature = `+ celsius + ` Celsius. `)